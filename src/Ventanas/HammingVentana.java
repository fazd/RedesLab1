    private void seleccBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seleccBActionPerformed
        JFileChooser fc = new JFileChooser();
        if(instance == ENVIO){
            fc.setFileFilter(new FileNameExtensionFilter("Text files", "txt"));
        }
        else{
            fc.setFileFilter(new FileNameExtensionFilter("ham files", "ham"));
        }
        int res = fc.showOpenDialog(this);
        if(res == JFileChooser.APPROVE_OPTION){
            File f = fc.getSelectedFile();
            switch(instance){
                
                case ENVIO:
                if(Validations.ValidFileEnvio(f,2)){
                    try {
                        file = f;
                        Scanner scan = new Scanner(f);
                        
                        String linea = scan.nextLine();
                        entradaTA.setText(linea);
                        rutaTF.setText(f.getAbsolutePath());


                    } catch (FileNotFoundException ex) {
                        JOptionPane.showMessageDialog(null,"El archivo Tiene problemas","Error",
                        JOptionPane.ERROR_MESSAGE);
                    }
                }
                break;
                
                case CORRECCION:
                    if(Validations.ValidFileHamm(f,12)){
                    try {
                        file = f;
                        Scanner scan = new Scanner(f);
                        while(scan.hasNext()){
                            String linea = scan.nextLine();
                            entradaTA.append(linea+"\n");
                            rutaTF.setText(f.getAbsolutePath());
                        }

                    } catch (FileNotFoundException ex) {
                        JOptionPane.showMessageDialog(null,"El archivo Tiene problemas","Error",
                        JOptionPane.ERROR_MESSAGE);
                    }
                }
                break;
        
            }
        }
    }//GEN-LAST:event_seleccBActionPerformed

    private void AccionBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AccionBActionPerformed
        try {
            Scanner scan = new Scanner(file);
            if(file != null){
                if(instance == ENVIO){
                    
                    String linea = scan.nextLine();
                    Hamming hamm = new Hamming(linea);
                    String n = file.getCanonicalPath().substring(0, file.getCanonicalPath().length()-4);
                    File out = hamm.createMessageFile(n);
                    JOptionPane.showMessageDialog(this, "El archivo fue creado exitosamente en "
                            + out.getAbsolutePath());
                    scan = new Scanner(out);
                    while(scan.hasNext()){
                        linea = scan.nextLine();
                        SalidaTA.append(linea+"\n");
                        
                        
                    }
                }
                else{
                    Scanner scanf = new Scanner(file);
                    while(scanf.hasNext()){
                        String line = scanf.nextLine();
                        Correccion cr = new Correccion(line);
                        String k = cr.verifyErrors();
                        SalidaTA.append(k);
                        
                    }
                }
            }
            else{
                 JOptionPane.showMessageDialog(null, "No ha cargado un archivo valido", "Error",
                    JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "El archivo Tiene problemas", "Error",
                    JOptionPane.ERROR_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "El archivo Tiene problemas", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_AccionBActionPerformed

